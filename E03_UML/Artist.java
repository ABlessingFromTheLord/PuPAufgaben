package E03_UML;

public class Artist {
    // Fields
    private String name;

    // Constructor
    public Artist(String name){
        this.name = name;
    }

    // Methods
    // Getters
    public String getName() {
        return this.name;
    }

    // Setters
    public void setName(String name) {
        this.name = name;
    }
}
